/* 
    * Inheritance is one of the key features of Object-oriented programming in C++. It allows us to create a new class (derived class) from an existing class (base class).
    >>  The derived class inherits the features from the base class and can have additional features of its own.
    >>  Reusability is a very important feature of OOPS,Inheritance help us to reduce code reusability. 
    >>  In c++ we can reuse a class and add additional features to it.
    >>  The existing class is called an the Base Class.
    >>  The new class which is inherited is called as the Derived class.

    * There are different types of inheritance in C++ 
    >>  1. Single inheritance
    >>  2. Multiple inheritance
    >>  3. Hierarchical inheritance
    >>  4. Multileval inheritance
    >>  5. Hybrid inheritance

    * Single Inheritance
      >> A derived class with only one Base class

    * Multiple Inheritance
      >> A derived class with More then one Base class

    * Hierarchical Inheritance
      >> Several derived class with only one Base class

    * Multileval Inheritance
      >> Deriving a class from already derived class

    * Hybrid Inheritance
      >> A combination of multiple inheritance and multilevel inheritance.
      >> A class is derived form two classes as in multiple inheritance.
      >> Hovever, one of the parent classes is not a base class.

 */