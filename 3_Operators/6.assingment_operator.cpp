#include <iostream>
using namespace std;

int main(void)
{
    // Assignment operators => = += -= *= /= %=

    int a = 10;
    a += 5;
    cout << a << '\n';

    a -= 5;
    cout << a << '\n';

    a *= 5;
    cout << a << '\n';

    a /= 5;
    cout << a << '\n';

    a %= 5;
    cout << a << '\n';
    return 0;
}